function [R]=matrixc_orth(kappa,mu)

lamda=kappa-2*mu/3;

C11=2*mu+lamda;
C22=2*mu+lamda*2;
C33=2*mu+lamda*3;  
C44=mu;
C55=1.5*mu;
C66=2*mu;
C12=C11-C66; 
C13=lamda*2;  
C23=lamda*3; 
R(1:3,1:3,1:3,1:3) = 0;


R(1,1,1,1)=C11;
R(2,2,2,2)=C22;
R(3,3,3,3)=C33;
R(2,3,2,3)=C44;
R(2,3,3,2)=C44;
R(3,2,3,2)=C44;
R(3,2,2,3)=C44;
R(2,1,2,1)=C66;
R(2,1,1,2)=C66;
R(1,2,2,1)=C66;
R(1,2,1,2)=C66;
R(1,3,1,3)=C55;
R(1,3,3,1)=C55;
R(3,1,1,3)=C55;
R(3,1,3,1)=C55;
R(1,1,2,2)=C12;
R(2,2,1,1)=C12;
R(1,1,3,3)=C13;
R(2,2,3,3)=C23;
R(3,3,1,1)=C13;
R(3,3,2,2)=C23;

